func TestListCategoriesSuccess(t *testing.T) {
	db := setupTestDB()
	truncateCategory(db)

	tx, _ := db.Begin()
	categoryRepository := repository.NewCategoryRepository()
	category1 := categoryRepository.Save(context.Background(), tx, domain.Category{
		Name: "Gadget",
	})
	category2 := categoryRepository.Save(context.Background(), tx, domain.Category{
		Name: "Shoe",
	})
	tx.Commit()

	router := setupRouter(db)

	request := httptest.NewRequest(http.MethodGet, "http://localhost:3000/api/categories", nil)
	request.Header.Add("X-API-Key", "RAHASIA")

	recorder := httptest.NewRecorder()
	router.ServeHTTP(recorder, request)

	response := recorder.Result()
	defer response.Body.Close()

	assert.Equal(t, http.StatusOK, response.StatusCode)

	body, _ := io.ReadAll(response.Body)
	var responseBody map[string]interface{}
	json.Unmarshal(body, &responseBody)

	categories := responseBody["data"].([]interface{})

	assert.Equal(t, category1.Id, int(categories[0].(map[string]interface{})["id"].(float64)))
	assert.Equal(t, category1.Name, categories[0].(map[string]interface{})["name"].(string))

	assert.Equal(t, float64(category2.Id), categories[1].(map[string]interface{})["id"].(float64))
	assert.Equal(t, category2.Name, categories[1].(map[string]interface{})["name"].(string))
}

